/*
-> Linear Search (Data Structures) — Short Explanation (Java Theory)

- Linear search is the simplest searching technique. It searches an element by checking each element 
  one by one from the beginning of the list until the element is found or the list ends.

-> How it works

- Start from the first element of the array.
- Compare each element with the target value.
- If a match is found → return the index.
- If the end is reached → element is not present.

-> Key points

- Works on sorted and unsorted arrays
- Easy to implement
- Less efficient for large data

-> Time Complexity

- Best case: O(1) (element found at first position)
- Worst case: O(n) (element at last position or not found)
- Average case: O(n)

-> Space Complexity

- O(1) (no extra memory used)

-> When to use

- Small datasets
- Unsorted data
- When simplicity is more important than speed

*/