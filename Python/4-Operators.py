# Arithmetic Operators
a = 10
b = 3
print("Arithmetic:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)
print("Floor Division:", a // b)

# Assignment Operators
print("\nAssignment:")
x = 5
x += 2
print("x after += 2:", x)
x *= 3
print("x after *= 3:", x)

# Comparison Operators
print("\nComparison:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)

# Logical Operators
print("\nLogical:")
print("a > 5 and b < 5:", a > 5 and b < 5)
print("a < 5 or b < 5:", a < 5 or b < 5)
print("not (a == b):", not (a == b))

# Identity Operators
print("\nIdentity:")
print("a is b:", a is b)
print("a is not b:", a is not b)

# Membership Operators
print("\nMembership:")
nums = [1, 2, 3, 10]
print("10 in nums:", 10 in nums)
print("5 not in nums:", 5 not in nums)

# Bitwise Operators
# a = 1010
# b = 0011
print("\nBitwise:")
print("a & b (AND):", a & b)
print("a | b (OR):", a | b)
print("a ^ b (XOR):", a ^ b)
print("~a (NOT):", ~a)
print("a << 1 (left shift):", a << 1)
print("a >> 1 (right shift):", a >> 1)
